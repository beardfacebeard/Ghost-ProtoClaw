import { spawn } from "node:child_process";
import { setTimeout as delay } from "node:timers/promises";
import { fileURLToPath } from "node:url";
import path from "node:path";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const projectRoot = path.resolve(__dirname, "..");
const prismaBin = path.join(projectRoot, "node_modules", "prisma", "build", "index.js");
const nextBin = path.join(projectRoot, "node_modules", "next", "dist", "bin", "next");

function runNodeBin(binPath, args, label, options = {}) {
  return new Promise((resolve, reject) => {
    const child = spawn(process.execPath, [binPath, ...args], {
      cwd: projectRoot,
      env: process.env,
      stdio: options.input !== undefined ? ["pipe", "inherit", "inherit"] : "inherit"
    });

    if (options.input !== undefined && child.stdin) {
      child.stdin.write(options.input);
      child.stdin.end();
    }

    child.on("error", (error) => {
      reject(new Error(`${label} failed to start: ${error.message}`));
    });

    child.on("exit", (code, signal) => {
      if (signal) {
        reject(new Error(`${label} exited because of signal ${signal}`));
        return;
      }

      if (code === 0) {
        resolve();
        return;
      }

      reject(new Error(`${label} exited with code ${code ?? "unknown"}`));
    });
  });
}

function preflightEnv() {
  // Fail fast with an actionable error if the template didn't wire up
  // the secrets the app needs at request time. Without this, the service
  // would bind the port, pass Railway's healthcheck, and then 500 on the
  // first auth/login request with a cryptic "Missing required environment
  // variable" stack trace buried in the logs.
  const checks = [
    {
      label: "SESSION_SECRET",
      value:
        process.env.SESSION_SECRET ||
        process.env.MISSION_CONTROL_SESSION_SECRET,
      hint: "64-char hex — auto-generated by the Railway template. Regenerate with: openssl rand -hex 32"
    },
    {
      label: "MAGIC_LINK_SECRET",
      value: process.env.MAGIC_LINK_SECRET,
      hint: "64-char hex — auto-generated by the Railway template. Regenerate with: openssl rand -hex 32"
    },
    {
      label: "ENCRYPTION_KEY",
      value:
        process.env.ENCRYPTION_KEY || process.env.INTEGRATION_ENCRYPTION_KEY,
      hint: "32-char hex — auto-generated by the Railway template. Regenerate with: openssl rand -hex 16"
    },
    {
      label: "DATABASE_URL",
      value: process.env.DATABASE_URL,
      hint: "Postgres connection string — Railway wires this from the Postgres service."
    },
    {
      label: "NEXT_PUBLIC_APP_URL",
      value: process.env.NEXT_PUBLIC_APP_URL,
      hint: "Public URL of the app — Railway sets this to https://${{ RAILWAY_PUBLIC_DOMAIN }}."
    },
    {
      label: "MISSION_CONTROL_ADMIN_EMAIL",
      value: process.env.MISSION_CONTROL_ADMIN_EMAIL,
      hint: "Admin login email — set in the Railway deploy wizard."
    },
    {
      label: "MISSION_CONTROL_ADMIN_PASSWORD",
      value: process.env.MISSION_CONTROL_ADMIN_PASSWORD,
      hint: "Admin login password — set in the Railway deploy wizard."
    }
  ];

  const missing = checks.filter(
    (check) => !check.value || String(check.value).trim().length === 0
  );

  if (missing.length === 0) {
    return;
  }

  console.error("");
  console.error("=".repeat(72));
  console.error("Ghost ProtoClaw cannot start: required environment variables are missing.");
  console.error("=".repeat(72));
  for (const check of missing) {
    console.error(`  - ${check.label}`);
    console.error(`      ${check.hint}`);
  }
  console.error("");
  console.error("If you deployed via Railway, open your project's Variables tab and");
  console.error("add the missing entries above. If the template was supposed to auto-");
  console.error("generate these and didn't, redeploy from the current template button:");
  console.error("  https://railway.com/deploy/S9YZU-");
  console.error("=".repeat(72));
  console.error("");
  process.exit(1);
}

// DIRECT_URL is required by Prisma's schema config. Railway's minimal template
// only wires DATABASE_URL, so mirror it into DIRECT_URL when unset so migrations
// and runtime queries share the same connection string.
function normalizeDirectUrl() {
  if (!process.env.DIRECT_URL && process.env.DATABASE_URL) {
    process.env.DIRECT_URL = process.env.DATABASE_URL;
  }
}

async function waitForDatabase() {
  // Railway can take 30-90 seconds to bring Postgres online on a fresh
  // project. Poll until the database is reachable before running any
  // Prisma commands so we don't burn migration attempts on a cold DB.
  const maxAttempts = 30;
  const delayMs = 5000;

  for (let attempt = 1; attempt <= maxAttempts; attempt += 1) {
    try {
      console.log(
        `Checking database connectivity (attempt ${attempt}/${maxAttempts})...`
      );
      await runNodeBin(prismaBin, ["db", "execute", "--stdin"], "db connectivity check", {
        input: "SELECT 1;"
      });
      console.log("Database is reachable.");
      return;
    } catch (error) {
      if (attempt === maxAttempts) {
        console.error(
          `Database still unreachable after ${maxAttempts} attempts (${(maxAttempts * delayMs) / 1000}s).`
        );
        throw error;
      }
      console.warn(
        `Database not ready (attempt ${attempt}/${maxAttempts}). Waiting ${delayMs / 1000}s...`
      );
      await delay(delayMs);
    }
  }
}

function printMigrationFailureHelp() {
  // Emit an actionable block when migrate deploy fails so the operator
  // knows exactly what to check and how to recover, instead of staring
  // at a bare Prisma stack trace.
  console.error("");
  console.error("=".repeat(72));
  console.error("prisma migrate deploy failed and the deploy is aborting.");
  console.error("=".repeat(72));
  console.error("");
  console.error("Common causes:");
  console.error(
    "  - A migration in prisma/migrations/ is malformed or references a"
  );
  console.error("    schema object that no longer exists.");
  console.error(
    "  - The database already contains tables that don't match what the"
  );
  console.error(
    "    migration history expects (schema drift from manual changes)."
  );
  console.error(
    "  - A pending migration is incompatible with existing data."
  );
  console.error("");
  console.error("What to do:");
  console.error(
    "  - Check the Prisma error printed above for the specific SQL failure."
  );
  console.error(
    "  - For a fresh database with no important data, you can force-sync the"
  );
  console.error(
    "    schema by redeploying with DATABASE_MIGRATION_MODE=push set. This"
  );
  console.error(
    "    bypasses the migration history and should NEVER be used on a"
  );
  console.error("    database that already holds production data.");
  console.error(
    "  - For an established database, restore from backup or repair the"
  );
  console.error(
    "    migration history with `prisma migrate resolve` before redeploying."
  );
  console.error("=".repeat(72));
  console.error("");
}

async function runDatabaseMigrations() {
  // Explicit opt-in for `prisma db push` mode. This is fine for the very
  // first deploy on an empty database (dev/demo/fresh Railway project) and
  // catastrophic on anything holding real data: db push rewrites the schema
  // to match prisma/schema.prisma with no migration history, which will
  // silently drop columns or tables if the schema has diverged. There is
  // NO automatic fallback to push if migrate deploy fails — the operator
  // must consent to this mode by setting the env var.
  const usePush =
    process.env.DATABASE_MIGRATION_MODE === "push" ||
    process.env.PRISMA_USE_DB_PUSH === "true";

  const args = usePush
    ? ["db", "push", "--skip-generate"]
    : ["migrate", "deploy"];
  const label = usePush ? "prisma db push" : "prisma migrate deploy";

  if (usePush) {
    console.warn(
      "WARNING: DATABASE_MIGRATION_MODE=push is set. This will force the database "
        + "schema to match prisma/schema.prisma and IGNORE migration history. Only "
        + "use this on a fresh database."
    );
  }

  // Wait for the database to be reachable first (handles Railway cold starts)
  try {
    await waitForDatabase();
  } catch (error) {
    console.error(
      "Could not reach the database. Check that your DATABASE_URL environment variable references the correct Postgres service in Railway."
    );
    throw error;
  }

  // Retry on transient failures only. Once waitForDatabase succeeds, the DB
  // is reachable — any subsequent migrate failure is almost always a real
  // migration problem and retrying won't help. A small number of retries
  // still covers brief network blips between the connectivity check and
  // the migrate call.
  const maxAttempts = 3;
  for (let attempt = 1; attempt <= maxAttempts; attempt += 1) {
    try {
      console.log(`Running ${label} (attempt ${attempt}/${maxAttempts})...`);
      await runNodeBin(prismaBin, args, label);
      return;
    } catch (error) {
      if (attempt === maxAttempts) {
        if (!usePush) {
          printMigrationFailureHelp();
        }
        throw error;
      }

      console.warn(
        `${label} failed on attempt ${attempt}. Retrying in 10 seconds...`
      );
      await delay(10000);
    }
  }
}

async function maybeSeed() {
  const shouldSeed =
    process.argv.includes("--seed") || process.env.SEED_ON_START === "true";

  if (!shouldSeed) {
    return;
  }

  console.log("Running prisma db seed...");
  await runNodeBin(prismaBin, ["db", "seed"], "prisma db seed");
}

let nextServer = null;

function shutdown(signal) {
  console.log(`Received ${signal}. Shutting down Ghost ProtoClaw...`);

  if (nextServer) {
    nextServer.kill(signal);
    return;
  }

  process.exit(0);
}

process.on("SIGTERM", () => shutdown("SIGTERM"));
process.on("SIGINT", () => shutdown("SIGINT"));

try {
  console.log("Ghost ProtoClaw starting up...");
  preflightEnv();
  normalizeDirectUrl();
  await runDatabaseMigrations();
  await maybeSeed();

  console.log("Starting Next.js server...");
  nextServer = spawn(process.execPath, [nextBin, "start"], {
    cwd: projectRoot,
    env: process.env,
    stdio: "inherit"
  });

  nextServer.on("error", (error) => {
    console.error("Next.js server failed to start.");
    console.error(error);
    process.exit(1);
  });

  nextServer.on("exit", (code, signal) => {
    if (signal) {
      process.kill(process.pid, signal);
      return;
    }

    process.exit(code ?? 0);
  });
} catch (error) {
  console.error(error instanceof Error ? error.message : error);
  process.exit(1);
}
